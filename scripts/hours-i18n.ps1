function Get-HoursI18n([string]$s, [string]$lang) {
  if (-not $s) { return $s }
  if ($lang -eq 'ja' -or -not $lang) { return $s }
  $idx = @{ 'en'=1; 'zh'=2; 'ko'=3; 'zh-tw'=4 }[$lang]
  if (-not $idx) { return $s }
  foreach ($p in $script:HoursRepl) { $s = $s.Replace($p[0], $p[$idx]) }
  return $s
}
